SELECT
ORD.STOFCY_0,
ORD.ITMREF_0,
SUM(ORD.SHTQTY_0)*(SELECT PRI_0 FROM LIVE.SPRICLIST WHERE PLI_0 = '11' and PLICRD_0 = ORD.ITMREF_0 AND PLICRI1_0 = 'PL2')

FROM LIVE.ORDERS ORD

WHERE ORD.VCRTYP_0 = 2 and ORD.ITMREF_0 IN ('BAS3901200','BAS4300101','BAS5901800','MGE10360162','MGEBP331B','WNN12212','WNN321CR','WNNPC503AD')

GROUP BY  ORD.STOFCY_0,ORD.ITMREF_0