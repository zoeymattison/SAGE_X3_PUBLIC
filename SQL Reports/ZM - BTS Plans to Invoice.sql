SELECT DISTINCT STOPRELIS.ORINUM_0, STOPRELIS.PRLNUM_0, STOPRELIS.PRHNUM_0, STOPREH.DLVFLG_0 FROM LIVE.STOPRELIS

LEFT OUTER JOIN LIVE.STOPREH ON STOPRELIS.PRHNUM_0 = STOPREH.PRHNUM_0

WHERE STOPRELIS.BPCORD_0 = ''

order by STOPRELIS.PRLNUM_0 asc
