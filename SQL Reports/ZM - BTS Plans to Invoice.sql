select SOHNUM_0, BPCORD_0, SDHNUM_0, SIHNUM_0 FROM LIVE.SDELIVERY WHERE BPCORD_0 = ''
AND CREDAT_0 >= GETDATE()-1

order by SOHNUM_0 asc

